const ELEMENTS = {APP_ROOT: '#root', HOME_CONTAINER: '[data-testid="home-container"]'}

Cypress.Commands.add("checkSiteIsUp", () => {
    cy.get(ELEMENTS.APP_ROOT).should('be.visible')
    cy.get(ELEMENTS.HOME_CONTAINER).should('be.visible')
})

